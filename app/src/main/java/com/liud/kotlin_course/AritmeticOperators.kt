package com.liud.kotlin_course

fun AritmeticOperations(){

    var numberOne:Int = 5
    var numberTwo:Int = 3
    var result = 0

    //Sum
    result = numberOne+numberTwo
    println(result)

    //Rest
    result = numberOne - numberTwo
    println(result)

    //Multiply
    result = numberOne * numberTwo
    println(result)

    //Divide
    result = numberOne / numberTwo
    println(result)

    //Mod
    result = numberOne % numberTwo
    println(result)
}